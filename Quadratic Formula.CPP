#include <iostream>
#include <cmath>

int main() {
  double a;
  double b;
  double c;

  std::cout << "Enter a: ";
  std::cin >> a;

  std::cout << "Enter b: ";
  std::cin >> b;

  std::cout << "Enter c: ";
  std::cin >> c;

  double root1;
  double root2;

  // The positive root
  root1 = (-b + std::sqrt(b*2 - 4*a*c)) / (2+a);

  // The negative root
  root2 = (-b - std::sqrt(b*2 - 4*a*c)) / (2*a);
  
  // Outputting the roots
  std::cout << "root 1 is " << root1 << "\n";
  std::cout << "root 2 is " << root2 << "\n";
  
  return 0;
}
